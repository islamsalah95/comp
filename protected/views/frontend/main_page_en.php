function sendContact() {
     var valid = validateContact();
    if(valid) {
        jQuery.ajax({
            url: "<?php echo $link->link('contact_us_form', frontend); ?>",
            data:'name='+$("#name").val()+'&email='+
            $("#email").val()+'&phone='+
            $("#phone").val(),
            type: "POST",
            dataType: 'text',
            success:function(data){
                $("#mail-status-success").html(data);
                $("#mail-status-success").html('<div id="alertFadeOut" class="alert alert-success">The service request has been sent successfully</div>'); // Diplay message with a fadeout
                    $('#alertFadeOut').fadeOut(3000, function () {
                        $('#alertFadeOut').text('');
                    }); 
                var form = document.getElementById("ContactUsForm");
                form.addEventListener('submit', function () {
                    form.submit.disabled = true;
                });
            },
            error:function(data){
                $("#mail-status-error").html(data);
                $("#mail-status-success").html('<div id="alertFadeOut" class="alert alert-danger col-12">Sorry, there was a problem sending the service!</div>'); // Diplay message with a fadeout
                    $('#alertFadeOut').fadeOut(3000, function () {
                        $('#alertFadeOut').text('');
                    }); 
            },
        });
    }
}

function validateContact() {
    var valid = true;	
    $(".demoInputBox").css('background-color','');
    $(".info").html('');
    if(!$("#name").val()) {
        $("#name-info").html("(required)").css('color','red');
        $("#name").css('background-color','#FFFFDF');
        valid = false;
    }
    if(!$("#email").val()) {
        $("#email-info").html("(required)").css('color','red');
        $("#email").css('background-color','#FFFFDF');
        valid = false;
    }
    if(!$("#email").val().match(/^([\w-\.]+@([\w-]+\.)+[\w-]{2,4})?$/)) {
        $("#email-info").html("(invalid)");
        $("#email").css('background-color','#FFFFDF');
        valid = false;
    }
    if(!$("#phone").val()) {
        $("#phone-info").html("(required)").css('color','red');
        $("#phone").css('background-color','#FFFFDF');
        valid = false;
    }
    return valid;
}
